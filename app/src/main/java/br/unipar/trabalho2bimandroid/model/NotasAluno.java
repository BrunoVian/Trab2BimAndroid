package br.unipar.trabalho2bimandroid.model;

public class NotasAluno {
    private int ra;
    private String nome;
    private int priBim;
    private int segBim;
    private int terBim;
    private int quaBim;
    private String disciplina;


    public NotasAluno() {

    }

    public int getRa() {
        return ra;
    }

    public void setRa(int ra) {
        this.ra = ra;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getPriBim() {
        return priBim;
    }

    public void setPriBim(int priBim) {
        this.priBim = priBim;
    }

    public int getSegBim() {
        return segBim;
    }

    public void setSegBim(int segBim) {
        this.segBim = segBim;
    }

    public int getTerBim() {
        return terBim;
    }

    public void setTerBim(int terBim) {
        this.terBim = terBim;
    }

    public int getQuaBim() {
        return quaBim;
    }

    public void setQuaBim(int quaBim) {
        this.quaBim = quaBim;
    }

    public String getDisciplina() {
        return disciplina;
    }

    public void setDisciplina(String disciplina) {
        this.disciplina = disciplina;
    }

}
